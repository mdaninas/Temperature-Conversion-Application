    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String inputString = input1.getText();
        String hasilsatuan = null;
        switch (tes.konver) {
            case "celcius":
                hasilsatuan = "C";
                break;
            case "fahrenheit":
                hasilsatuan = "F";
                break;
            case "reamur":
                hasilsatuan = "R";
                break;
        }
        float inputan = Float.parseFloat(inputString);
        tes.input = inputan;
        tes.hasil = tes.getSuhu();
        labelhasil2.setText("Dari " + tes.asal + " ke " + tes.konver);
        Hasil.setText(Float.toString(tes.hasil) + " Â°"+hasilsatuan);

    }//GEN-LAST:event_jButton2ActionPerformed

